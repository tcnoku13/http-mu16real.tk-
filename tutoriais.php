      <p> Botão Esquerdo do Mouse : Mover,           Correr, Atacar, Encostar ou Sentar<br />
        Botão Direito do Mouse : Usar magias e skills.<br />
        [Ctrl] + Botão Mouse : Ataque PK<br />
        F1 : Ajuda<br />
        F2 : Exibir / Esconder Diálogo<br />
        F3 : Mudar para modo de Whisper<br />
        F4 : Ajustar tamanho da janela de Bate-Papo<br />
        F5 : Abrir o Mussenger<br />
        F6 : Esconder o Mussenger<br />
        Enter : Janela de Bate-Papo<br />
        D : Comandos automáticos<br />
        M ou /Move : Mover entre mapas e mundos<br />
        C : Estatisticas de Personagem<br />
        I ou V : Inventário<br />
        Q : Usar poção de cura<br />
        W : Usar poção de mana<br />
        E : Usar antídoto ou itens de cura<br />
        F : Mussenger<br />
        I ou V, em seguida S : Abrir Loja Pessoal<br />
        D, em seguida [Comprar] : Comprar item de uma loja pessoal<br />
        I ou V, em seguida R : Reparar item<br />
        C, em seguida T : Status de Quest<br />
        C, em seguida Botão de Pet : Status de seus Pets<br />
        [Ctrl] + Número : Gerar atalho para magia / skill<br />
        Número de 1 ~ 9 : Selecionar magia / skill<br />
        [Alt] : Exibir nome dos itens no solo<br />
        [Alt] + Botão Direito do Mouse : Selecionar item no solo<br />
        /número + mensagem : Gravar mensagem automática<br />
        [Alt] + número : Exibir mensagem gravada<br />
        Barra de Espaço : Pegar itens no chão automaticamente<br />
        Print Screen : Tirar foto da tela de jogo<br />
        /&lt;nome do item&gt; : Informações sobre o item<br />
        /Request &lt;on / off&gt;: Ligar / Desligar pedidos de party, duelo ou           trade.<br />
        /Duel (apontando para o jogador) : Desafiar jogador para um duelo<br />
        /post &lt;mensagem&gt; : Mensagem para todos os jogadores<br />
  <br />
